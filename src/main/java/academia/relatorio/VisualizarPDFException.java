
package academia.relatorio;

public class VisualizarPDFException extends Exception {

    public VisualizarPDFException() {
    }

    public VisualizarPDFException(String string) {
        super(string);
    }

    public VisualizarPDFException(String string, Throwable thrwbl) {
        super(string, thrwbl);
    }

    public VisualizarPDFException(Throwable thrwbl) {
        super(thrwbl);
    }

    public VisualizarPDFException(String string, Throwable thrwbl, boolean bln, boolean bln1) {
        super(string, thrwbl, bln, bln1);
    }
    
}
